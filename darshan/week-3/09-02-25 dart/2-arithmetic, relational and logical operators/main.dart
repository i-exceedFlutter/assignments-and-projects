void main() {
  //Airthemetic operation
  int x = 110;
  int y = 25;
  print("x: ${x} y: ${y}");
  print("x + y : ${x + y}");
  print("x - y : ${x - y}");
  print("x * y : ${x * y}");
  print("x / y : ${x / y}");
  print("x ~/ y: ${x ~/ y}");
  print("x % y : ${x % y}");

  //relational operation
  x = 10;
  y = 25;
  print("x < y : ${x < y}");
  print("x > y : ${x > y}");
  print("x <= y : ${x <= y}");
  print("x >= y : ${x >= y}");
  print("x == 10 : ${x == 10}");
  print("x != y : ${x != y}");

  //Logical operation
  print("true || false :${true || false}");
  print("true && false :${true && false}");
  print("!true :${!true}");
}
